#ifndef SUM_LIB_H
#define SUM_LIB_H

// Структура для передачи данных в поток.
// Потоковая функция может принимать только один аргумент (void*),
// поэтому мы "упаковываем" все, что нам нужно, в одну структуру.
struct SumArgs {
    int *array;
    int begin;
    int end;
};

// Функция, которую будет выполнять каждый поток.
// Она принимает указатель на структуру SumArgs и возвращает
// указатель на посчитанную сумму (long long*).
void *sum_part(void *args);

#endif // SUM_LIB_H